import unittest

from day4 import solve_a, solve_b, is_winner

DATA = [
    [22, 13, 17, 11, 0, 8, 2, 23, 4, 24, 21, 9, 14, 16, 7, 6, 10, 3, 18, 5, 1, 12, 20, 15, 19],
    [3, 15, 0, 2, 22, 9, 18, 13, 17, 5, 19, 8, 7, 25, 23, 20, 11, 10, 24, 4, 14, 21, 16, 12, 6],
    [14, 21, 17, 24, 4, 10, 16, 15, 9, 19, 18, 8, 23, 26, 20, 22, 11, 13, 6, 5, 2, 0, 12, 3, 7]
]


class TestDay4(unittest.TestCase):
    def test_solve_a(self):
        self.assertEqual(4512, solve_a(
            [7, 4, 9, 5, 11, 17, 23, 2, 0, 14, 21, 24, 10, 16, 13, 6, 15, 25, 12, 22, 18, 20, 8, 19, 3, 26, 1],
            DATA
        ))

    def test_solve_b(self):
        self.assertEqual(1924, solve_b(
            [7, 4, 9, 5, 11, 17, 23, 2, 0, 14, 21, 24, 10, 16, 13, 6, 15, 25, 12, 22, 18, 20, 8, 19, 3, 26, 1],
            DATA
        ))

    def test_is_winner(self):
        self.assertEqual(True, is_winner([-1, -1, -1, -1, -1,
                                          10, 16, 15, 9, 19,
                                          18, 8, 23, 26, 20,
                                          22, 11, 13, 6, 5,
                                          2, 0, 12, 3, 7]))
        self.assertEqual(True, is_winner([14, 21, -1, 24, 4,
                                          10, 16, -1, 9, 19,
                                          18, 8, -1, 26, 20,
                                          22, 11, -1, 6, 5,
                                          2, 0, -1, 3, 7]))
        self.assertEqual(False, is_winner([-1, -1, -1, -1, 4,
                                           10, 16, 15, 9, 19,
                                           18, 8, 23, 26, 20,
                                           22, 11, 13, 6, 5,
                                           2, 0, 12, 3, 7]))
        self.assertEqual(False, is_winner([-1, -1, -1, -1, 4,
                                           10, 16, -1, 9, 19,
                                           18, 8, -1, 26, 20,
                                           22, 11, -1, 6, 5,
                                           2, 0, 12, 3, 7]))
        self.assertEqual(False, is_winner([-1, 21, 17, 24, 4,
                                           10, -1, 15, 9, 19,
                                           18, 8, -1, 26, 20,
                                           22, 11, 13, -1, 5,
                                           2, 0, 12, 3, -1]))
        self.assertEqual(False, is_winner([14, 21, 17, 24, -1,
                                           10, 16, 15, -1, 19,
                                           18, 8, -1, 26, 20,
                                           22, -1, 13, 6, 5,
                                           -1, 0, 12, 3, 7]))


if __name__ == "__main__":
    unittest.main()
